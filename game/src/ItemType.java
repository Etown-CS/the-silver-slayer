public enum ItemType {
    /*
     * Junk: Worthless; wastes space
     * Health: Something that modifies the player's health, either positively or negatively
     * Armor: A piece of armor. Improves defense.
     * Weapon: An instrument of violence. Improves attack.
     * Wearable: An equippable that's neither armor nor weapon.
     */

    Unassigned, Junk, Health, Armor, Weapon, Wearable, Quest, Key;
    
}